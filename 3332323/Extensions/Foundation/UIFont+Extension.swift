import Foundation
import UIKit

extension UIFont {
    class func pingFangSCMedium(_ size: CGFloat)-> UIFont {
        return UIFont(name: "PingFangSC-Medium", size: size)!
    }
    
    class func pingFangRegular(_ size: CGFloat)-> UIFont {
        return UIFont(name: "PingFangSC-Regular", size: size)!
    }
}
